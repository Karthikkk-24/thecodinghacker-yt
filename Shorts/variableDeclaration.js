// Variable Declaration In JavaScript

// Variables

// Var Keyword
var name = "thecodinghacker";
console.log(name); // thecodinghacker
name = "javascript";
console.log(name); // javascript

// Let Keyword
let name2 = "javascript";
console.log(name2); // thecodinghacker
name2 = "typescript";
console.log(name2); // javascript

// Constants
const age = 21;
console.log(age);

age = 22;
console.log(age); // error: redeclaring of constants
